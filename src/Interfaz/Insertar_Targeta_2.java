    private ImageIcon montar_imagen() throws SQLException{
        ImageIcon imagenI;
        imagenI = new ImageIcon();
        
        try{
        byte [] imagenBytes;
        
        int id = tipo.getId();
        int cant = controladora.descargar_imagen(id).length;
        imagenBytes = new byte[cant];
        imagenBytes = controladora.descargar_imagen(id);
        
        imagenI = new ImageIcon(imagenBytes);    
        
        imagenI = new ImageIcon(imagenI.getImage().getScaledInstance(this.imagen.getWidth(), this.imagen.getHeight(), Image.SCALE_DEFAULT));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "No se pudo cargar la imagen.");
        }
        return imagenI;
    }
    
    private String valor_targeta(){
        String valor;
        int precio = (int) tipo.getValor();
        if(tipo.getCuc())
            valor = String.valueOf(precio) + " CUC";
        else
            valor = String.valueOf(precio) + " MN";
        
        return valor;
    }
    
